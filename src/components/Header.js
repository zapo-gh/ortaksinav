import React from 'react';
import {
  AppBar,
  Toolbar,
  Typography,
  Button,
  Box,
  IconButton,
  Menu,
  MenuItem,
  Avatar,
  Chip
} from '@mui/material';
import {
  School as SchoolIcon,
  Menu as MenuIcon,
  AccountCircle,
  ExitToApp,
  Login,
  Home as HomeIcon
} from '@mui/icons-material';

const Header = ({ baslik, kullanici, onHomeClick, onTestDashboardClick }) => {
  const [anchorEl, setAnchorEl] = React.useState(null);
  const [showTestDashboard, setShowTestDashboard] = React.useState(false);
  const [lastKeyPress, setLastKeyPress] = React.useState(0);

  // Test Dashboard gÃ¶rÃ¼nÃ¼rlÃ¼ÄŸÃ¼nÃ¼ kontrol et
  React.useEffect(() => {
    const checkTestDashboardVisibility = () => {
      // URL parametresi kontrolÃ¼ - basit ?test
      const urlParams = new URLSearchParams(window.location.search);
      const showFromUrl = urlParams.has('test') || urlParams.get('showTestDashboard') === 'true';
      
      // localStorage kontrolÃ¼
      const isVisibleFromStorage = localStorage.getItem('show_test_dashboard') === 'true';
      
      // URL parametresi varsa Ã¶ncelik ver
      const isVisible = showFromUrl || isVisibleFromStorage;
      
      setShowTestDashboard(isVisible);
    };

    checkTestDashboardVisibility();

    // Klavye kÄ±sayolu handler - debounced
    const handleKeyDown = (e) => {
      const now = Date.now();
      
      // Debounce: 500ms iÃ§inde aynÄ± tuÅŸ basÄ±lÄ±rsa ignore et
      if (now - lastKeyPress < 500) {
        return;
      }
      
      // Test Dashboard toggle fonksiyonu
      const toggleTestDashboard = (keyName) => {
        console.log(`âœ… ${keyName} algÄ±landÄ±!`);
        setLastKeyPress(now);
        setShowTestDashboard(prev => {
          const newVisibility = !prev;
          localStorage.setItem('show_test_dashboard', newVisibility.toString());
          console.log(`ðŸ§ª ${keyName} - Test Dashboard:`, newVisibility ? 'AÃ§Ä±k' : 'KapalÄ±');
          return newVisibility;
        });
      };
      
      // Ctrl+Alt+T kombinasyonu
      if ((e.ctrlKey || e.metaKey) && e.altKey && (e.key === 't' || e.key === 'â‚º')) {
        e.preventDefault();
        toggleTestDashboard('Ctrl+Alt+T');
      }
      
      // Ctrl+Shift+T kombinasyonu
      else if ((e.ctrlKey || e.metaKey) && e.shiftKey && e.key === 'T') {
        e.preventDefault();
        toggleTestDashboard('Ctrl+Shift+T');
      }
      
      // F12 tuÅŸu - preventDefault() olmadan (console aÃ§Ä±lsÄ±n)
      else if (e.key === 'F12' || e.keyCode === 123) {
        toggleTestDashboard('F12');
      }
      
      // Escape tuÅŸu
      else if (e.key === 'Escape') {
        toggleTestDashboard('Escape');
      }
      
      // Space tuÅŸu (alternatif)
      else if (e.key === ' ') {
        e.preventDefault();
        toggleTestDashboard('Space');
      }
    };

    // Event listener'larÄ± ekle
    document.addEventListener('keydown', handleKeyDown);
    window.addEventListener('keydown', handleKeyDown);
    
    return () => {
      document.removeEventListener('keydown', handleKeyDown);
      window.removeEventListener('keydown', handleKeyDown);
    };
  }, []); // Dependency array'i boÅŸ bÄ±rak

  const handleMenu = (event) => {
    setAnchorEl(event.currentTarget);
  };

  const handleClose = () => {
    setAnchorEl(null);
  };

  return (
    <AppBar position="static" sx={{ bgcolor: 'primary.main', boxShadow: 2 }}>
      <Toolbar>
        {/* Logo ve BaÅŸlÄ±k */}
        <SchoolIcon sx={{ mr: 2, fontSize: 32 }} />
        <Typography 
          variant="h6" 
          component="div" 
          sx={{ 
            flexGrow: 1,
            fontWeight: 'bold',
            fontSize: '1.5rem'
          }}
        >
          {baslik || ''}
        </Typography>

        {/* MenÃ¼ ButonlarÄ± */}
        <Box sx={{ display: { xs: 'none', md: 'flex' }, mr: 2 }}>
          <Button 
            color="inherit" 
            sx={{ mr: 2 }} 
            onClick={onHomeClick}
            startIcon={<HomeIcon />}
          >
            Ana Sayfa
          </Button>
          {showTestDashboard && (
            <Button color="inherit" sx={{ mr: 2 }} onClick={onTestDashboardClick}>
              Test Dashboard
            </Button>
          )}
        </Box>

        {/* KullanÄ±cÄ± BÃ¶lgesi */}
        {kullanici ? (
          <Box sx={{ display: 'flex', alignItems: 'center', gap: 1 }}>
            <Chip 
              label={`HoÅŸ geldin, ${kullanici.ad}`}
              variant="outlined"
              sx={{ 
                color: 'white', 
                borderColor: 'white',
                '&:hover': {
                  bgcolor: 'rgba(255, 255, 255, 0.1)'
                }
              }}
            />
            <IconButton
              size="large"
              onClick={handleMenu}
              color="inherit"
            >
              <AccountCircle />
            </IconButton>
            <Menu
              anchorEl={anchorEl}
              open={Boolean(anchorEl)}
              onClose={handleClose}
            >
              <MenuItem onClick={handleClose}>
                <AccountCircle sx={{ mr: 1 }} />
                Profil
              </MenuItem>
              <MenuItem onClick={handleClose}>
                <ExitToApp sx={{ mr: 1 }} />
                Ã‡Ä±kÄ±ÅŸ
              </MenuItem>
            </Menu>
          </Box>
        ) : (
          <Button 
            color="inherit" 
            startIcon={<Login />}
            sx={{
              border: '1px solid rgba(255, 255, 255, 0.3)',
              '&:hover': {
                bgcolor: 'rgba(255, 255, 255, 0.1)'
              }
            }}
          >
            GiriÅŸ Yap
          </Button>
        )}

        {/* Mobil MenÃ¼ */}
        <IconButton
          size="large"
          color="inherit"
          sx={{ display: { xs: 'flex', md: 'none' }, ml: 1 }}
        >
          <MenuIcon />
        </IconButton>
      </Toolbar>
    </AppBar>
  );
};

export default Header;
